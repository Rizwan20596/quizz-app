const getRandomObjects = (questArr, noOfRecs) => {
  const result = [];
  const len = questArr.length;
  const taken = new Set();

  if (noOfRecs > len) {
    throw new RangeError(`Failed: The records requested is out of bounds. you can request max ${len} records.`);
  }

  while (result.length < noOfRecs) {
    const randIndex = Math.floor(Math.random() * len);
    if (!taken.has(randIndex)) {
      taken.add(randIndex);
      result.push(questArr[randIndex]);
    }
  }

  return result;
}

exports.getQuestions = (noOfRecords) => {
  const randomQuestions = getRandomObjects(questions, noOfRecords);
  return randomQuestions;
};
